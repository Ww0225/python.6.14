# 在一个序列a=(a[1],a[2],,a[n])中，如果(i,)满足i<j且
# a[2>a[j,则称为一个逆序对。
# 例如：(3,2,2,1)中包含6个逆序对。
# 请问，
# (87,39,35,1,99,10,54,1,46,24,74,62,49,13,2,80,24,58,8,14
# 中包含多少个逆序对？
nums = [87,39,35,1,99,10,54,1,46,24,74,62,49,13,2,80,24,58,8,14,83,23,97,85,3,2,86,10,71,15]
count = 0
for i in range(len(nums)):
  for j in range(i+1,len(nums)):
    if nums[i] > nums[j]:
      count += 1
print(count)